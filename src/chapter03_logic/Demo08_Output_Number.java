package chapter03_logic;

public class Demo08_Output_Number {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.print(i);
        }
        System.out.println(/* 换行 */);
        for (int i = 5; i >= 1; i--) {
            System.out.print(i);
        }
    }
}
